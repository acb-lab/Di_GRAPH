        if [[ -f "${subdir}/${sample}_75nt_${chr}_MATa_avg.tsv" ]]; then
          awk -F'\t' -v sample="$numeric_sample" 'BEGIN{OFS="\t"} {print sample, $0}' "${subdir}/${sample}_75nt_${chr}_MATa_avg.tsv" > "${subdir}/${sample}_75nt_${chr}_MATa_avg_ordered.tsv"
