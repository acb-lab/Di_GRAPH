# Create a log and stat file
current_time=$(date "+%d-%m-%Y %H:%M:%S")
log_file="${MYWD}/log_file.txt"
stats="${MYWD}/stats.txt"

echo "CONCORDANT ANALYSIS - initiated at: \"$current_time\"" > "$log_file"
