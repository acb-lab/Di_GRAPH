echo "=========  Bowtie Alignment ==========" >> "$stats"

# Start timer for the entire script processing
start_time_total_script=$SECONDS

# Index RG
bowtie-build "${MYREF}/RG_PMV_v9.fasta" "${MYREF}/S_cerevisiae_indexed" #NMQ --> MOVE TO INSTALL!


###########################################
