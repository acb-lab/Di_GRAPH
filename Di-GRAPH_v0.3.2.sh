          awk -F'\t' -v sample="$numeric_sample" 'BEGIN{OFS="\t"} {print sample, $0}' "${subdir}/${sample}_75nt_${chr}_avg.tsv" > "${subdir}/${sample}_75nt_${chr}_avg_ordered.tsv"
          
